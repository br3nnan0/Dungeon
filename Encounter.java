import java.util.Random;
public class Encounter
{
    private String eventName;
    private String mobName;
    private int mobHP;
    private int mobAP;
    Random rand = new Random();

    public Encounter(String event, String mobTitle, int mobAttack, int mobHealth)
    {
        event = eventName;
        mobTitle = mobName;
        mobAttack = mobAP;
        mobHealth = mobHP;
    }

}